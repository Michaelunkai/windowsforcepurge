max_workers = 20  # adjust based on your CPU/network capacity

def fetch_id(title):
    name, year = title.rsplit("(", 1)
    year = year.rstrip(")")
    try:
        results = ia.search_movie(name.strip(), results=5)  # limit to top 5
        for r in results:
            if r.get('year') == int(year):
                return f"tt{r.movieID}"
        if results:
            return f"tt{results[0].movieID}"
    except Exception:
        pass
    return "NOT_FOUND"

